package com.demo.entity;

public class User {	
	
	    private String name;  
	    private String loginTime;	  	    

		public String getLoginTime() {
			return loginTime;
		}

		public void setLoginTime(String loginTime) {
			this.loginTime = loginTime;
		}		
	  
	    public String getName() {  
	        return name;  
	    }  
	  
	    public void setName(String name) {  
	        this.name = name;  
	    }  
	  	    
}
